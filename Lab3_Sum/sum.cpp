#include <iostream>

using std::cout; using std::cin; using std::endl;

int main() {
	int first = 0, num = 1, sum = 0;
	
	cout << "Enters integer. Enter 0 to exit." << endl;
	cin >> first;

	if (first >= 0)
		sum = first;

	while (num != 0) {
		cin >> num;
		if (num >= 0)
			sum = sum + num;
	}

	cout << "The sum of the positive integers you entered is " << sum;
}
